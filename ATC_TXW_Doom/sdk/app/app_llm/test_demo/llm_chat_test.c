static int32 llm_chat_test_event_msg_process(void)
{
    struct llm_chat_test_event_msg event_msg = {0};

    RB_INT_GET(&chat_mgr.event_queue, event_msg);
    if (event_msg.event == LLM_EVENT_UNKNOWN) { return RET_OK; }

    switch (event_msg.event) {
        case LLM_EVENT_CHAT_RESULT: {
            os_printf("CHAT RESULT: %d\r\n", event_msg.msg_len);
            hgprintf_out(event_msg.msg, event_msg.msg_len, 0);
            _os_printf("\r\n");
            llm_free(event_msg.msg);
            break;
        }
        case LLM_EVENT_ERROR_MSG: {
            os_printf("recv CHAT err msg(%d):", event_msg.msg_len);
            hgprintf_out(event_msg.msg, event_msg.msg_len, 0);
            _os_printf("\r\n");
            llm_free(event_msg.msg);
            // TODO
            break;
        }
        case LLM_EVENT_CONN_ERR:
        case LLM_EVENT_TX_ERR:
        case LLM_EVENT_RX_ERR: {
            os_printf("CHAT %s err!\r\n", event_msg.event == LLM_EVENT_CONN_ERR ? "CONN" : (event_msg.event == LLM_EVENT_TX_ERR ? "TX" : "RX"));
            break;
        }
        default: {
            os_printf("Not supported event msg: %d\r\n", event_msg.event);
            break;
        }
    }

    return RET_OK;
}

int32 llm_chat_test_app_init(char *llm_name)
{
    int32 ret = RET_OK;
    struct llm_chat_test_event_msg *event_queue_buf = NULL;
    llm_global_init(&global);

    chat_mgr.chat_session = llm_chat_init(llm_name, &chat_session_cfg);
    if (!chat_mgr.chat_session) {
        os_printf("chat session init fail!\r\n");
        return RET_ERR;
    }
    ret = llm_chat_config(chat_mgr.chat_session, LLM_CONFIG_TYPE_TRANS,
                            (void *)&chat_trans_cfg, sizeof(chat_trans_cfg));
    if (ret) {
        os_printf("chat_trans_cfg fail!\r\n");
        return RET_ERR;
    }
    ret = llm_chat_config(chat_mgr.chat_session, LLM_CONFIG_TYPE_MODEL,
                            (void *)&llm_chat_cfg, sizeof(llm_chat_cfg));
    if (ret) {
        os_printf("llm_chat_cfg fail!\r\n");
        return RET_ERR;
    }
    
    sys_event_take(SYS_EVENT(SYS_EVENT_NETWORK, 0), llm_chat_test_network_event, 0);

    event_queue_buf = llm_malloc((LLM_CHAT_TEST_EVENT_QUEUE_CNT + 1) * sizeof(struct llm_chat_test_event_msg));
    if (!event_queue_buf) {
        os_printf("event_queue_buf malloc fail, no memory!\r\n");
        return RET_ERR;
    }
    RB_INIT_R(&chat_mgr.event_queue, LLM_CHAT_TEST_EVENT_QUEUE_CNT, event_queue_buf);

    cJSON_Hooks hooks = {0};
    hooks.malloc_fn = llm_malloc;
    hooks.free_fn = llm_free;
    cJSON_InitHooks(&hooks);

    os_printf("Waiting for network connection...");
    while (!sys_status.wifi_connected || !sys_status.dhcpc_done) {
        _os_printf(".");
        os_sleep(1);
    }
    os_printf("Network connected!\r\n");

    return ret;
}

void llm_chat_test_main(void)
{
    int32 ret = RET_OK;
    char *chat_recv = NULL;
    int32 chat_recv_len = 0;

    ret = llm_chat_test_app_init("doubao_llm_chat");
    if (ret) {
        os_printf("chat main init failed: %d\r\n", ret);
        goto cleanup;
    }
    chat_recv = llm_malloc(1024);
    if (!chat_recv) {
        os_printf("no mem!\r\n");
        goto cleanup;
    }

    os_printf("chat main init start\r\n");
    llm_chat_upload_file(chat_mgr.chat_session, LLM_DATA_TYPE_IMAGE, LLM_IMAGE_TYPE_JPEG, (char *)test_image, test_image_size);
    llm_chat_send(chat_mgr.chat_session, LLM_DATA_TYPE_TEXT, LLM_DATA_STATE_START, "图片主要讲了什么?", os_strlen("图片主要讲了什么?"));

    while (1) {
        llm_chat_test_event_msg_process();
        chat_recv_len = llm_chat_recv(chat_mgr.chat_session, chat_recv, 1024);
        if (chat_recv_len > 0) {
            os_printf("CHAT RESULT: %d\r\n", chat_recv_len);
            hgprintf_out(chat_recv, chat_recv_len, 0);
            _os_printf("\r\n");
        }
        os_sleep_ms(100);
    }

cleanup:
    if (chat_mgr.event_queue.rbq) llm_free(chat_mgr.event_queue.rbq);
    llm_chat_deinit(chat_mgr.chat_session);
    llm_global_deinit();
}

